    public function store(Request $request)
    {
        Book::create($request->validate([
            'title' => 'required',
            'author' => 'required',
            'publisher_name' => 'required',
            'published_year' => 'required|integer',
            'category_id' => 'required|exists:categories,id'
        ]));
